public class BitwiseOperators {
    public static void main(String[] args) {
        System.out.println(5&6);//Binary AND

        System.out.println(5|6);//OR

        System.out.println(5^6);//XOR

        System.out.println(~0);//one's compliment

        System.out.println(1<<2);//left shift

        System.out.println(5>>1);//Right shift
    }
}
