public class Unaryoperators {
    public static void main(String[] args) {
        int A = 5;
        
        System.out.println("A = " +(A++));//post increment

        System.out.println(A);
        
        System.out.println("A = " +(++A));//pre increment
        System.out.println("A = " +(A--));
        System.out.println("A = " +(--A));
    }
}
